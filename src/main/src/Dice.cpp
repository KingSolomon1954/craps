unsigned
Dice::roll()
{
    d1_ = 6;
    d2_ = 5;
    return d1_ + d2_;
}

/*-----------------------------------------------------------*//**

*/
unsigned
Dice::value() const
{
    return d1_ + d2_;
}

/*-----------------------------------------------------------*//**

*/
unsigned
Dice::d1() const
{
    return d1_;
}

/*-----------------------------------------------------------*//**

*/
unsigned
Dice::d2() const
{
    return d2_;
}

